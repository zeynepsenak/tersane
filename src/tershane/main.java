    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        antwort.setText("");
        
        String pass;
        String idd = idno.getText();
        int idn = Integer.parseInt(idd);
        
        pass = ps.getText();
        
        func.db_con();
        String log_ant = func.login(idn, pass);
            
        if(log_ant.equals("Admin")){
            new admincontrol(idd).setVisible(true);
            this.setVisible(false);
        }else if(log_ant.equals("Mitarbeiter")){
            new mitarbeiter().setVisible(true);
            this.setVisible(false);
        }else if(log_ant.equals("not exist")){
            antwort.setText("ID Information oder Passwort ist falsch!");
        }else{
            antwort.setText("Probieren Sie wieder bitte! Ein Fehler ist aufgetreten");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

